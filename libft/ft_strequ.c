#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2);

int	ft_strequ(char const *s1, char const *s2)
{
	int i;

	i = 0;
	if (s1 == NULL || s2 == NULL)
		return (0);
	if (s1[i] != '\0' && s2[i] != '\0')
		if (ft_strcmp(s1, s2))
				return (0);
	return (1);
}
